class Solution:
    def compress(self, chars: 'List[str]') -> 'int':

        count = 1
        i = 0
        while i < len(chars)-1:
            if chars[i+1] == chars[i]:
                count += 1
                i += 1
            else:
                if count > 1:
                    index = i - count + 1
                    chars[index+1: index+count] = list(str(count))
                    i += - count + 2 + len(list(str(count)))
                    count = 1
                else:
                    i += 1
        if count != 1:
            index = i - count +1
            chars[index + 1: index + count] = list(str(count))
        return chars


